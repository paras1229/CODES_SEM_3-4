class exp1
{
    public static void main(String[] args)
    {
        //declaration and initialization of 1D array
        int a[]=new int[5];
        a[0]=10;
        a[1]=20;
        a[2]=30;
        a[3]=40;
        a[4]=50;

        //traversing 1D array using for loop
        for(int i=0;i<a.length;i++)
        {
            System.out.println(a[i]);
        }
    }
}