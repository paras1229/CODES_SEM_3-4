import java.util.*;
class incr_dicr
{
        public static void main(String[] args)
        {
                int a=10, b=10;
                System.out.println("a++ is " +(a++));
                System.out.println("++a is " +(++a));

                System.out.println("b-- is " +(b--));
                System.out.println("--b is " +(--b));
        }
}